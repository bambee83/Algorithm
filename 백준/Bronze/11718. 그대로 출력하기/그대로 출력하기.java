import java.io.*;
import java.util.*;

public class Main {
    public static void main(String[] args)  throws IOException {
        Scanner sc = new Scanner(System.in);

        while (sc.hasNextLine()) { // 입력이 더 있는 동안 반복
            String line = sc.nextLine(); // 한 줄 읽기
            System.out.println(line); // 그대로 출력
        }
        
            }
}
